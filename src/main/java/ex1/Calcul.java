package ex1;

/**
 * Classe qui permet de faire des calculs
 * 
 * @author Cécile Peyras
 *
 */
public class Calcul {

	/**
	 * Méthode qui permet de faire une addition
	 * 
	 * @param a
	 * @param b
	 * @return le résultat de l'addition
	 */
	public static int addition(int a, int b) {
		return a + b;
	}

	/**
	 * Méthode qui permet de faire une soustraction
	 * 
	 * @param a
	 * @param b
	 * @return le résultat d'une soustraction
	 */
	public static int soustraction(int a, int b) {
		return a - b;
	}
}
